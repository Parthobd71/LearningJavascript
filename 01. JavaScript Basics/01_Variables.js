// Variables in JS
var name = "John"; // 'var' is function-scoped and can be redeclared
let age = 30; // 'let' is block-scoped and cannot be redeclared 
const isStudent = false; // 'const' is block-scoped and cannot be reassigned
// Note: Prefer 'let' and 'const' over 'var' for better scoping
console.log("Name:", name);
console.log("Age:", age);
console.log("Is Student:", isStudent);
// End of Variables in JS



